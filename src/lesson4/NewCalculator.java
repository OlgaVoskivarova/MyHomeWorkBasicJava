package lesson4;

public class NewCalculator {

    double a;
    double b;

    public double add(double a, double b) {
        double result = a + b;
        return result;
    }

    public double div(double a,double b ) {
        double result = a / b;
        return result;
    }

    public double mult(double a, double b) {
        double result = a * b;
        return result;
    }

    public double sub(double a,double b) {
        double result = a - b;
        return result;

    }
}